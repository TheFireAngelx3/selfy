<?php
namespace backend\models;

class Request
{
    private string $path;
    private array $params; // GET
    private array $body;   // POST
    private string $type;

    public function __construct()
    {
        $this->setPath($_SERVER['REQUEST_URI']);
        $this->setBody($_POST);
        $this->setParams($_GET);
        $this->setType($_SERVER['REQUEST_METHOD']);
    }

    public function getBodyParam(string $param){
        return $this->getBody()[$param];
    }

    /**
     * @return string
     */
    public function getPath(): string
    {
        return $this->path;
    }

    /**
     * @param string $path
     */
    public function setPath(string $path): void
    {
        $this->path = $path;
    }

    /**
     * @return array
     */
    public function getParams(): array
    {
        return $this->params;
    }

    /**
     * @param array $params
     */
    public function setParams(array $params): void
    {
        $this->params = $params;
    }

    /**
     * @return array
     */
    public function getBody(): array
    {
        return $this->body;
    }

    /**
     * @param array $body
     */
    public function setBody(array $body): void
    {
        $this->body = $body;
    }

    /**
     * @return string
     */
    public function getType(): string
    {
        return $this->type;
    }

    /**
     * @param string $type
     */
    public function setType(string $type): void
    {
        $this->type = $type;
    }
}
